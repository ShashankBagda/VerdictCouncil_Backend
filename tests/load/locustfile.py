# Task sets
# ---------------------------------------------------------------------------


class CaseCRUDTasks(TaskSet):
    """Lightweight CRUD tasks simulating a judge managing cases."""

    case_ids: list[str] = []

    def on_start(self):
        # Create one case to have something to read
        self._create_case()

    def _create_case(self) -> str | None:
        payload = {
            "title": f"Load test case {_rand_str()}",
            "description": "Auto-generated by Locust load tests.",
            "domain": "traffic_violation",
            "parties": [
                {"name": "Prosecution", "role": "prosecution"},
                {"name": f"Defendant-{_rand_str(4)}", "role": "accused"},
            ],
        }
        with self.client.post(
            "/api/v1/cases",
            json=payload,
            catch_response=True,
            name="[cases] create",
        ) as resp:
            if resp.status_code in (200, 201):
                resp.success()
                data = resp.json()
                case_id = data.get("id") or data.get("case_id")
                if case_id:
                    self.case_ids.append(str(case_id))
                return str(case_id)
            else:
                resp.failure(f"Create failed: {resp.status_code}")
                return None

    @task(5)
    def list_cases(self):
        with self.client.get(
            "/api/v1/cases?limit=20&offset=0",
            catch_response=True,
            name="[cases] list",
        ) as resp:
            if resp.status_code == 200:
                elapsed = resp.elapsed.total_seconds() * 1000
                if elapsed > _THRESHOLD_CASE_PROCESS:
                    resp.failure(f"list_cases p95 exceeded: {elapsed:.0f}ms > {_THRESHOLD_CASE_PROCESS}ms")
                else:
                    resp.success()
            else:
                resp.failure(f"List failed: {resp.status_code}")

    @task(3)
    def get_case(self):
        if not self.case_ids:
            return
        case_id = random.choice(self.case_ids)
        with self.client.get(
            f"/api/v1/cases/{case_id}",
            catch_response=True,
            name="[cases] get",
        ) as resp:
            if resp.status_code == 200:
                resp.success()
            elif resp.status_code == 404:
                resp.success()  # Might have been deleted by another user
            else:
                resp.failure(f"Get failed: {resp.status_code}")

    @task(1)
    def create_case(self):
        self._create_case()

    @task(2)
    def dashboard(self):
        with self.client.get(
            "/api/v1/dashboard/stats",
            catch_response=True,
            name="[dashboard] stats",
        ) as resp:
            if resp.status_code in (200, 404):
                resp.success()
            else:
                resp.failure(f"Dashboard failed: {resp.status_code}")


class HealthCheckTasks(TaskSet):
    """Lightweight health-probe tasks that every VU runs."""

    @task(10)
    def health(self):
        with self.client.get(
            "/health",
            catch_response=True,
            name="[health] check",
        ) as resp:
            if resp.status_code == 200:
                resp.success()
            else:
                resp.failure(f"Health check failed: {resp.status_code}")

    @task(1)
    def pair_health(self):
        with self.client.get(
            "/api/v1/health/pair",
            catch_response=True,
            name="[health] pair",
        ) as resp:
            if resp.status_code in (200, 503):  # 503 = PAIR down (expected in load env)
                resp.success()
            else:
                resp.failure(f"PAIR health failed: {resp.status_code}")


class SSEStreamTasks(TaskSet):
    """SSE subscription task — opens stream for 2 s and closes."""

    _case_id: str | None = None

    def on_start(self):
        self._case_id = VC_CASE_ID or None

    @task
    def subscribe_case_sse(self):
        if not self._case_id:
            return  # Skip if no case ID configured
        start = time.monotonic()
        with self.client.get(
            f"/api/v1/cases/{self._case_id}/stream",
            stream=True,
            catch_response=True,
            timeout=5,
            name="[sse] subscribe",
        ) as resp:
            elapsed = (time.monotonic() - start) * 1000
            if resp.status_code in (200, 204):
                resp.success()
            elif resp.status_code == 404:
                resp.success()  # Case may not be running
            else:
                resp.failure(f"SSE failed: {resp.status_code} ({elapsed:.0f}ms)")


