"""Locust load test suite for VerdictCouncil API.

Covers the performance targets defined in docs/architecture/12-testing-summary.md:
  - case-processing p95 ≤ 5 s
  - evidence-analysis p95 ≤ 30 s
  - argument-construction p95 ≤ 45 s
  - full pipeline p95 ≤ 3 min (excluding Gate pauses)

Usage
-----
    # Headless (CI mode) — run 60 s burst, 10 users, ramp 2/s:
    locust -f tests/load/locustfile.py \
        --headless -u 10 -r 2 -t 60s \
        --host http://localhost:8000 \
        --html tests/load/report.html

    # Interactive UI (development):
    locust -f tests/load/locustfile.py --host http://localhost:8000

Environment variables
---------------------
    VC_LOAD_USERNAME    judge username to authenticate as (default: judge@example.com)
    VC_LOAD_PASSWORD    password for that user (default: TestPassword123!)
    VC_LOAD_CASE_ID     an existing case UUID to hit pipeline endpoints against
                        (default: empty — case creation task creates one dynamically)

CI integration
--------------
The ``locustfile.py`` is invoked in the CI ``load-tests`` job defined in
``.github/workflows/ci.yml``.  Failure thresholds are enforced via:
    --exit-code-on-error 1
    --check-response-time  (when supported)
The CI job fails if p95 > threshold or error_rate > 5%.

Notes
-----
* This file intentionally has no pytest markers — Locust runs it directly,
  not through pytest.
* ``--csv=results`` emits results.csv / results_failures.csv / results_stats.csv
  which are uploaded as CI artifacts.
"""

from __future__ import annotations

import os
import random
import string
import time
from typing import Any

from locust import HttpUser, TaskSet, between, events, task

# ---------------------------------------------------------------------------
# Configuration (override via env)
# ---------------------------------------------------------------------------

VC_USERNAME = os.environ.get("VC_LOAD_USERNAME", "judge@example.com")
VC_PASSWORD = os.environ.get("VC_LOAD_PASSWORD", "TestPassword123!")
VC_CASE_ID = os.environ.get("VC_LOAD_CASE_ID", "")

# Performance thresholds (milliseconds) — from 12-testing-summary.md
_THRESHOLD_CASE_PROCESS = 5_000      # p95 ≤ 5 s
_THRESHOLD_EVIDENCE = 30_000         # p95 ≤ 30 s
_THRESHOLD_ARGUMENTS = 45_000        # p95 ≤ 45 s
_THRESHOLD_FULL_PIPELINE = 180_000   # p95 ≤ 3 min


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _rand_str(n: int = 8) -> str:
    return "".join(random.choices(string.ascii_lowercase, k=n))


# ---------------------------------------------------------------------------
# Auth + case bootstrap task set
# ---------------------------------------------------------------------------


class AuthMixin:
    """Mixin that handles login before task execution starts."""

    _token_cookie: str | None = None

    def on_start(self):
        """Authenticate once and store the JWT cookie."""
        with self.client.post(
            "/api/v1/auth/login",
            json={"username": VC_USERNAME, "password": VC_PASSWORD},
            catch_response=True,
            name="[auth] login",
        ) as resp:
            if resp.status_code == 200:
                resp.success()
            else:
                resp.failure(f"Login failed: {resp.status_code} {resp.text[:200]}")


