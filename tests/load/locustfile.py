class JudgeUser(AuthMixin, HttpUser):
    """Simulates a judge performing typical CRUD operations."""

    wait_time = between(1, 3)
    tasks = {CaseCRUDTasks: 4, HealthCheckTasks: 1}

    def on_start(self):
        super().on_start()  # Login


class HealthCheckUser(HttpUser):
    """Simulates a monitoring probe hitting health endpoints."""

    wait_time = between(5, 10)
    tasks = [HealthCheckTasks]


class SSEUser(AuthMixin, HttpUser):
    """Simulates a browser tab holding an SSE connection open."""

    wait_time = between(30, 60)  # SSE holds longer connections
    tasks = [SSEStreamTasks]

    def on_start(self):
        super().on_start()


# ---------------------------------------------------------------------------
# Custom failure thresholds hook (checked after test run)
# ---------------------------------------------------------------------------


@events.quitting.add_listener
def assert_performance_thresholds(environment, **kwargs):
    """Fail the run if p95 latency or error rate exceed configured thresholds."""
    stats = environment.runner.stats

    error_rate = (
        stats.total.num_failures / max(stats.total.num_requests, 1)
    )
    if error_rate > 0.05:
        environment.process_exit_code = 1
        print(
            f"\n[LOAD TEST FAIL] Error rate {error_rate:.1%} exceeds 5% threshold"
        )

    # Check p95 for the most critical endpoints
    for name in ("[cases] list", "[cases] get", "[health] check"):
        entry = stats.entries.get((name, "GET")) or stats.entries.get((name, "POST"))
        if entry and entry.get_response_time_percentile(0.95) > _THRESHOLD_CASE_PROCESS:
            p95 = entry.get_response_time_percentile(0.95)
            environment.process_exit_code = 1
            print(
                f"\n[LOAD TEST FAIL] {name!r} p95={p95:.0f}ms exceeds "
                f"{_THRESHOLD_CASE_PROCESS}ms threshold"
            )
